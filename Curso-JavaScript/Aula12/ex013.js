var agora = new Date();
var diaSem = agora.getDay();

diaSem = 7
/*
    0 = Domingo
    1 = Segunda-feira
    2 = Terça-feira
    3 = Quarta-feira
    4 = Quinta-feira
    5 = Sexta-feira
    6 = Sábado
*/

//console.log(diaSem)

switch(diaSem) {
    case 0:
        console.log('Domingo');
        break;
    case 0:
        console.log('Domingo');
        break;
    case 0:
        console.log('Domingo');
        break;
    case 0:
        console.log('Domingo');
        break;
    case 0:
        console.log('Domingo');
        break;
    case 0:
        console.log('Domingo');
        break;
    case 0:
        console.log('Domingo');
        break;
    default:
        console.log('[ERRO] Dia inválido!');
        break;
}